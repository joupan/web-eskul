function displayResults(results) {
    const resultContainer = document.getElementById('result');
    resultContainer.innerHTML = '';

    if (results.length === 0) {
        resultContainer.innerHTML = '<p>Tidak ada hasil yang ditemukan.</p>';
    } else {
        results.forEach(eskul => {
            const eskulElement = document.createElement('div');
            eskulElement.classList.add('eskul-item');
            eskulElement.innerHTML = `
                <h2>${eskul.name}</h2>
                <p>${eskul.description}</p>
                <ul>
                    ${eskul.details.map(detail => `<li>${detail}</li>`).join('')}
                </ul>
                 <div class="image-gallery">
                    ${eskul.images.map(image => `<img src="${image}" alt="${eskul.name}" class="gallery-image">`).join('')}
                </div>
            `;
            resultContainer.appendChild(eskulElement);
        });
    }

    resultContainer.classList.add('show');
}

function searchEskul(query) {
    return eskulData.filter(eskul => 
        eskul.name.toLowerCase().includes(query.toLowerCase())
    );
}


function displayAllEskul() {
    const resultContainer = document.getElementById('result');
    resultContainer.innerHTML = '<ul class="eskul-list">';

    eskulData.forEach(eskul => {
        resultContainer.innerHTML += `
            <li>${eskul.name}</li>
        `;
    });

    resultContainer.innerHTML += '</ul>';
    resultContainer.classList.add('show');
}

document.addEventListener('DOMContentLoaded', function() {
    displayAllEskul(); 

    document.getElementById('search-input').addEventListener('input', function(e) {
        const query = e.target.value.trim(); 
        const resultContainer = document.getElementById('result');

        const minLength = 3;

        if (query.length >= minLength) { 
            const results = searchEskul(query); 
            displayResults(results); 
        } else if (query.length === 0) {
            displayAllEskul(); 
        } else {
            resultContainer.innerHTML = ''; 
            resultContainer.classList.remove('show'); 
        }
    });
});